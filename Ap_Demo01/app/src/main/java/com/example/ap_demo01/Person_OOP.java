package com.example.ap_demo01;

public class Person_OOP
{
    private String name;
    private int age;

    public Person_OOP(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }
}
